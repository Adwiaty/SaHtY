import axios from "axios";

export default {
  data: function () {
    return {
      username: "",
      email: "",
      address: "",
      location: "",
      phoneNumber: "",
    };
  },
  methods: {
    getpharmacyData: function () {
      var id = JSON.parse(sessionStorage.getItem("session")).id;
      axios
        .get(`http://localhost:5000/pharmacies/profile/${id}`)
        .then(({ data }) => {
          this.username = data.username;
          this.location = data.location;
          this.email = data.email;
          this.phoneNumber = data.phoneNumber;
          this.address = data.address;
        })
        .catch((err) => {
          console.log(err);
        });
    },
    updateprofile: function () {
      var userdata = {
        username: this.username,
        email: this.email,
        address: this.address,
        location: this.location,
        phoneNumber: this.phoneNumber,
      };
      var _id = JSON.parse(sessionStorage.getItem("session")).id;

      axios
        .put(`http://localhost:5000/pharmacies/update/${_id}`, userdata)
        .then(({ data }) => {
          console.log("response from server :", data);
          this.$router.go();
        })
        .catch((err) => {
          console.log(err);
        });
    },
  },
  created: function () {
    this.getpharmacyData();
  },
};
</script>
